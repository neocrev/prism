export function banner(text) {
  const upper = text.toUpperCase();
  const glyphs = upper.split('').map(ch => FONT[ch] || FONT[' ']);
  if (!glyphs.length) return '';
  const lines = [];
  for (let row = 0; row < 5; row++) {
    lines.push(glyphs.map(g => g[row]).join(' '));
  }
  return lines.join('\n');
}
